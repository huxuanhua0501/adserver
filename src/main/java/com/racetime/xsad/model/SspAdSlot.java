public class SspAdSlot {
	private String ssp_devicegroup_id;
	private Double price;
	private String app_id;
	private String ssp_adslot_id;
	private String name;
	private String media_name;
	public String getSsp_devicegroup_id() {
		return ssp_devicegroup_id;
	}
	public void setSsp_devicegroup_id(String ssp_devicegroup_id) {
		this.ssp_devicegroup_id = ssp_devicegroup_id;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getApp_id() {
		return app_id;
	}
	public void setApp_id(String app_id) {
		this.app_id = app_id;
	}
	public String getSsp_adslot_id() {
		return ssp_adslot_id;
	}
	public void setSsp_adslot_id(String ssp_adslot_id) {
		this.ssp_adslot_id = ssp_adslot_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMedia_name() {
		return media_name;
	}
	public void setMedia_name(String media_name) {
		this.media_name = media_name;
	}
	
	
	
	
}
